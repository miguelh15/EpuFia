package bean;

public class PreMatriculaBean {

	int codPreMat;
	String codPre,fecha,situacion;
	public int getCodPreMat() {
		return codPreMat;
	}
	public void setCodPreMat(int codPreMat) {
		this.codPreMat = codPreMat;
	}
	public String getCodPre() {
		return codPre;
	}
	public void setCodPre(String codPre) {
		this.codPre = codPre;
	}
	public String getFecha() {
		return fecha;
	}
	public void setFecha(String fecha) {
		this.fecha = fecha;
	}
	public String getSituacion() {
		return situacion;
	}
	public void setSituacion(String situacion) {
		this.situacion = situacion;
	}
	
	
}
