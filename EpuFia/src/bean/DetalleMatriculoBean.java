package bean;

public class DetalleMatriculoBean {
	
	int codTipoC;
	String tipo,descripcion;
	public int getCodTipoC() {
		return codTipoC;
	}
	public void setCodTipoC(int codTipoC) {
		this.codTipoC = codTipoC;
	}
	public String getTipo() {
		return tipo;
	}
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	
	
}
